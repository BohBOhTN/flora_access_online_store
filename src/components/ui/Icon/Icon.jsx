function Icon({ 
  name, 
  size = 24, 
  color = 'currentColor',
  fill = 'none',
  strokeWidth = 2,
  className = '',
  ...props 
}) {
  const iconPath = icons[name];
  
  if (!iconPath) {
    console.warn(`Icon "${name}" not found`);
    return null;
  }

  return (
    <svg
      viewBox="0 0 24 24"
      width={size}
      height={size}
      fill={fill}
      stroke={color}
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      className={className}
      aria-hidden="true"
      {...props}
    >
      {iconPath}
    </svg>
  );
}

